let aNumber = 30;
let aString = "Learning JavaScript";
let aBoolean = true;
let anObject = { language: "JavaScript", type: "programming" };
let anArray = [10, 20, 30, 40];

console.log(aNumber, typeof aNumber); // Output: 30 number
console.log(aString, typeof aString); // Output: Learning JavaScript string
console.log(aBoolean, typeof aBoolean); // Output: true boolean
console.log(anObject, typeof anObject); // Output: { language: "JavaScript", type: "programming" } object
console.log(anArray, typeof anArray); // Output: [10, 20, 30, 40] object
